"""Backtest the project's per-circuit neutralisation probabilities.

The simulator prices every lap with a per-circuit probability of a Full Course
Yellow / Safety Car. This script asks the honest question those numbers invite:
**do they actually predict, out-of-sample, better than just knowing the
long-run base rate?** It leaves each race edition out, forms the probability
from the *other* editions only, and grades the result with proper scoring
rules (Brier, log-loss, Brier skill vs climatology) plus a reliability curve —
across all three series with identical code.

Two grouping levels are scored so the answer is not a single unfalsifiable
number:

- **by circuit** — the level the simulator uses.
- **by series** — a positive control: IMSA and WEC genuinely differ in FCY
  frequency, so a working harness must show skill here even where it shows none
  by circuit.

Outputs (offline; reads only committed derived CSVs)::

    python scripts/run_prediction_backtest.py

    data/derived/prediction/neutralisation_calibration.csv  — one row per (target, level)
    data/derived/prediction/neutralisation_reliability.csv   — reliability-curve bins
    reports/prediction/calibration.md                        — the write-up
"""

from __future__ import annotations

import sys
from pathlib import Path

import pandas as pd

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

from src.ingestion.config import (  # noqa: E402
    ENDURANCE_DERIVED_DIR,
    F1_DERIVED_DIR,
    PREDICTION_DERIVED_DIR,
    PREDICTION_REPORTS_DIR,
)
from src.prediction.backtest import (  # noqa: E402
    endurance_race_table,
    f1_race_table,
    outcomes_by,
    score_backtest,
)


def _targets() -> list[tuple[str, pd.DataFrame, str]]:
    """(label, per-race table, outcome column) for every neutralisation target,
    each table already restricted to races where the hazard is defined."""
    flags = pd.read_csv(ENDURANCE_DERIVED_DIR / "race_flags.csv")
    end = endurance_race_table(flags)
    f1 = f1_race_table(pd.read_csv(F1_DERIVED_DIR / "sc_model.csv"))

    imsa, wec = end[end["series"] == "imsa"], end[end["series"] == "wec"]
    return [
        # IMSA has no Safety Car at all (SC omitted — nothing to predict).
        ("IMSA FCY", imsa, "fcy"),
        ("WEC FCY", wec, "fcy"),
        ("WEC Safety Car", wec, "sc"),
        ("F1 Safety Car", f1, "sc"),
        ("F1 VSC", f1, "vsc"),
        # Positive control: the pooled endurance FCY, split by series.
        ("Endurance FCY (by series)", end, "fcy"),
    ]


def _run() -> tuple[pd.DataFrame, pd.DataFrame]:
    summary_rows, reliability_rows = [], []
    for label, table, col in _targets():
        # The positive control groups by series; everything else by circuit.
        level = "series" if label.endswith("(by series)") else "circuit"
        res = score_backtest(label, outcomes_by(table, col, level))
        row = res.summary_row()
        row.update({"target": label, "level": level})
        summary_rows.append(row)
        for b in res.reliability:
            reliability_rows.append({"target": label, "predicted": round(b.predicted, 4),
                                     "observed": round(b.observed, 4), "count": b.count})
    cols = ["target", "level", "n_races", "base_rate", "brier",
            "brier_climatology", "skill", "log_loss"]
    return pd.DataFrame(summary_rows)[cols], pd.DataFrame(reliability_rows)


def _report(summary: pd.DataFrame) -> str:
    def fmt(r):
        verdict = "beats base rate" if r["skill"] > 0 else "no better than base rate"
        return (f"| {r['target']} | {r['level']} | {r['n_races']} | {r['base_rate']:.2f} "
                f"| {r['brier']:.3f} | {r['brier_climatology']:.3f} | {r['skill']:+.3f} | {verdict} |")

    lines = [
        "# Calibration — do our per-circuit neutralisation odds actually predict?",
        "",
        "The strategy simulator prices every lap with a per-circuit probability of a",
        "Full Course Yellow or Safety Car. This report holds those numbers to the only",
        "standard that matters for a forecast: **out-of-sample skill**. Each race",
        "edition is left out, its probability is formed from the *other* editions of",
        "the same circuit (Jeffreys `(k+0.5)/(n+1)`), and the prediction is graded with",
        "proper scoring rules. `src/prediction/`, regenerated by",
        "`scripts/run_prediction_backtest.py`.",
        "",
        "The **Brier skill score** is the headline: the fractional improvement in Brier",
        "score over predicting the base rate every time. Positive means the grouping",
        "genuinely forecasts; zero means it adds nothing over the long-run average;",
        "negative means it is *worse* than knowing nothing.",
        "",
        "| Target | Level | Races | Base rate | Brier | Brier (base rate) | Skill | Verdict |",
        "|---|---|---|---|---|---|---|---|",
    ]
    lines += [fmt(r) for _, r in summary.iterrows()]
    lines += [
        "",
        "## What this says (and it is not the flattering answer)",
        "",
        "**Per circuit, the neutralisation odds do not beat the base rate.** At 6-8 F1",
        "editions and 3-11 endurance editions per circuit, the leave-one-out per-circuit",
        "frequency is dominated by sampling noise: its skill is around zero or negative",
        "for every target. Knowing *which* circuit you are at does not, on this evidence,",
        "let you out-predict the series-wide rate of a Safety Car.",
        "",
        "**The harness is not rigged to fail, though — it finds signal where signal",
        "exists.** The positive control (endurance FCY grouped by *series*) has clearly",
        "positive skill, because IMSA (~0.97 FCY) and WEC (~0.73) genuinely differ. A",
        "backtest that only ever returned zero would be untestable; this one",
        "distinguishes a real effect (series) from noise (circuit), which is the whole",
        "point of scoring a prediction instead of admiring a fit.",
        "",
        "**So this is a limitation the project states about its own simulator, with a",
        "number attached.** The per-circuit FCY/SC probabilities are still the best",
        "available inputs and their posteriors already carry wide credible intervals",
        "(the safety-car reports say so); what this backtest adds is the proof that the",
        "*point estimates* should be read as little more than the series rate until many",
        "more editions accumulate. The rolling pipeline keeps ingesting them, and this",
        "report is regenerated each time — so the day a circuit does earn measurable",
        "skill, the table will show it.",
        "",
        "## Method notes",
        "",
        "- **Proper scoring rules only.** Brier and log-loss are minimised only by",
        "  reporting your true belief, so the score cannot be gamed by hedging.",
        "- **Leave-one-race-out.** A race never contributes to its own prediction, so",
        "  the skill is genuinely out-of-sample, not an in-sample fit.",
        "- **Jeffreys smoother** `(k+0.5)/(n+1)` keeps a circuit seen once at an",
        "  uninformative 0.5 rather than a falsely-certain 0 or 1.",
        "- **F1 outcomes** are reconstructed from the committed per-circuit occurrence",
        "  counts (`data/derived/f1/sc_model.csv`); the leave-one-out prediction depends",
        "  only on the event count `k` and edition count `n`, never on which season, so",
        "  the counts are sufficient. Endurance outcomes come per-race from the lap-level",
        "  flag table (`data/derived/endurance/race_flags.csv`).",
        "- **IMSA Safety Car is omitted**: there is none in the data, so there is nothing",
        "  to predict (see the IMSA simulator report).",
        "",
    ]
    return "\n".join(lines)


def main() -> None:
    summary, reliability = _run()
    PREDICTION_DERIVED_DIR.mkdir(parents=True, exist_ok=True)
    PREDICTION_REPORTS_DIR.mkdir(parents=True, exist_ok=True)
    summary.to_csv(PREDICTION_DERIVED_DIR / "neutralisation_calibration.csv", index=False)
    reliability.to_csv(PREDICTION_DERIVED_DIR / "neutralisation_reliability.csv", index=False)
    (PREDICTION_REPORTS_DIR / "calibration.md").write_text(_report(summary), encoding="utf-8")

    print(summary.to_string(index=False))
    print(f"\nwrote {PREDICTION_DERIVED_DIR / 'neutralisation_calibration.csv'}")
    print(f"wrote {PREDICTION_DERIVED_DIR / 'neutralisation_reliability.csv'}")
    print(f"wrote {PREDICTION_REPORTS_DIR / 'calibration.md'}")


if __name__ == "__main__":
    main()
